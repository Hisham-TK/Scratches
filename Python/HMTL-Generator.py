# ---------------------------------------------------------------------------------------------------------------------
#                                               Generate Years Descending
# ---------------------------------------------------------------------------------------------------------------------
start = -2018
stop = -1900 + 1
step = 1

for number in range(start, stop, step):
    str_number = str(number)[1:]
    print('<option value="{var}">{var}<option>'.format(var=str_number))

# ---------------------------------------------------------------------------------------------------------------------
#                                               Generate Years Ascending
# ---------------------------------------------------------------------------------------------------------------------
start = 1900
stop = 2018 + 1
step = 1

for number in range(start, stop, step):
    print('<option value="{var}">{var}<option>'.format(var=number))

# ---------------------------------------------------------------------------------------------------------------------
#                                             Generate Tables: Rows and Columns
# ---------------------------------------------------------------------------------------------------------------------
table_row = 15
