# ---------------------------------------------------------------------------------------------------------------------
#                                                  Generate Ordered List
# ---------------------------------------------------------------------------------------------------------------------
number_of_lists = 9
items = 9

for o in range(number_of_lists):
    print("<ol>")
    for item_o in range(items):
        print("    <li></li>")
    print("</ol>")

# ---------------------------------------------------------------------------------------------------------------------
#                                                  Generate Unordered List
# ---------------------------------------------------------------------------------------------------------------------
number_of_lists = 9
items = 9

for u in range(number_of_lists):
    print("<ul>")
    for item_u in range(items):
        print("    <li></li>")
    print("</ul>")
